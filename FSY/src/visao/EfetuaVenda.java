    public void atualizarTabela() {
        DefaultTableModel modeloTabela;
        modeloTabela = (DefaultTableModel) tabItens.getModel();

        while (modeloTabela.getRowCount() > 0) {
            modeloTabela.removeRow(0);
        }
        valorTotal = 0;
        for (ItemVenda v : this.listaItens) {
            modeloTabela.addRow(new Object[]{v.getRoupa(), v.getRoupa().getValorVenda(), v.getQuantidade(), v.getSubTotal()});
            valorTotal += v.getSubTotal();
        }
        txtValorTotal.setText(String.valueOf(valorTotal));
    }
    
