    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtCpf.getText().equals("   .   .   /  ")) {
            BuscaCliente bc = new BuscaCliente(null, true);
            bc.setVisible(true);
            if (bc.isConfirmou()) {
                clienteVenda = bc.getClienteSelecionado();
                txtCpf.setText(clienteVenda.getCpf());
            }
        } else if (clienteVenda != null) {
            BuscaCliente bc = new BuscaCliente(null, true);
            bc.setVisible(true);
            if (bc.isConfirmou()) {
                clienteVenda = bc.getClienteSelecionado();
                txtCpf.setText(clienteVenda.getCpf());
            }
        } else {
            ControleCliente cc = new ControleCliente();
            clienteVenda = cc.buscaCPF(txtCpf.getText());
            if (clienteVenda == null) {
                JOptionPane.showMessageDialog(this, "Não foi encontrado nenhum cliente com esse CPF", "Não Localizado", JOptionPane.WARNING_MESSAGE);
                txtCpf.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Cliente Encontrado: \n" + clienteVenda);
                txtCpf.setEditable(false);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtCodigo.getText().isEmpty()) {
            BuscaRoupa br = new BuscaRoupa(null, true);
            br.setVisible(true);
            if (br.isConfirmou()) {
                roupaAtual = br.getRoupaSelecionada();
                txtDescricao.setText(roupaAtual.getDescricao());
                txtValor.setText(String.valueOf(roupaAtual.getValorVenda()));
            }
        } else {
            try {
                int codigo = Integer.parseInt(txtCodigo.getText());
                ControleRoupa cr = new ControleRoupa();
                roupaAtual = cr.buscaPorCodigo(codigo);
                if (roupaAtual == null) {
                    JOptionPane.showMessageDialog(this, "Não foi encontrada nenhuma roupa com esse código de barras", "Não Localizado", JOptionPane.WARNING_MESSAGE);
                    txtCodigo.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Roupa Encontrada");
                    txtDescricao.setText(roupaAtual.getDescricao());
                    txtValor.setText(String.valueOf(roupaAtual.getValorVenda()));
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "O código foi preenchido incorretamente\n" + e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (roupaAtual != null && !(txtQuantidade.getText().isEmpty())) {
            try {
                int qtd = Integer.parseInt(txtQuantidade.getText());
                if (roupaAtual.getQtdEstoque() < qtd || qtd < 1) {
                    JOptionPane.showMessageDialog(this, "A quantidade não pode ser maior que o estoque da roupa", "Quantidade Invalida", JOptionPane.WARNING_MESSAGE);
                } else {
                    boolean naLista = false;
                    for (ItemVenda v : listaItens) {
                        if (v.getRoupa().equals(roupaAtual)) {
                            qtd = qtd + v.getQuantidade();
                            if (roupaAtual.getQtdEstoque() < qtd) {
                                JOptionPane.showMessageDialog(this, "A quantidade não pode ser maior que o estoque da roupa", "Quantidade Invalida", JOptionPane.WARNING_MESSAGE);
                            } else {
                                v.setQuantidade(qtd);
                            }
                            naLista = true;
                        }
                    }
                    if (!naLista) {
                        ItemVenda iv = new ItemVenda();
                        iv.setRoupa(roupaAtual);
                        iv.setQuantidade(qtd);
                        listaItens.add(iv);
                    }
                    atualizarTabela();
                    txtCodigo.setText("");
                    txtDescricao.setText("");
                    txtQuantidade.setText("");
                    txtValor.setText("");
                    roupaAtual = null;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "A quantidade foi preenchida incorretamente\n" + e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

