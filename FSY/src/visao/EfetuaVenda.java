    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linha = tabItens.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this,
                    "Selecione um item na tabela");
        } else {
            int resposta;
            resposta = JOptionPane.showConfirmDialog(this,
                    "Confirma Remoção?",
                    "Remoção de Item",
                    JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (resposta == JOptionPane.YES_OPTION) {
                listaItens.remove(listaItens.get(linha));
                atualizarTabela();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (listaItens.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pelo menos um Item deve ser vendido", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                int cod = Integer.parseInt(txtNrNota.getText());
                if (ControleVenda.buscaPorCodigo(cod) == null) {
                    v.setNrNotaFiscal(cod);
                    v.setCliente(clienteVenda);
                    v.setData(data);
                    v.setFuncionario(ControleFuncionario.getUsuarioLogado());
                    for (ItemVenda iv : listaItens) {
                        iv.setVenda(v);
                    }
                    v.setListaItens(listaItens);
                    v.setValorTotal(valorTotal);

                    OpcoesPagamento op = new OpcoesPagamento(null, true);
                    if (clienteVenda == null) {
                        op.setClienteIdentificado(false);
                    } else {
                        op.setClienteIdentificado(true);
                    }
                    op.setV(v);
                    op.setVisible(true);
                    if (op.isConfirmou()) {
                        v = op.getV();
                        confirmou = true;
                        JOptionPane.showMessageDialog(rootPane, "Venda concluida com sucesso!");
                        this.dispose();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Já existe uma venda com esse numero da nota fiscal.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "O numero da nota fiscal foi preenchido incorretamente\n" + e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
                txtNrNota.requestFocus();
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

