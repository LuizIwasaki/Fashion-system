    public void atualizarTabela(List<Venda> lista) {
        DefaultTableModel modeloTabela;
        modeloTabela = (DefaultTableModel) tabVenda.getModel();

        while (modeloTabela.getRowCount() > 0) {
            modeloTabela.removeRow(0);
        }

        listaVendas.clear();

        listaVendas.addAll(lista);
        SimpleDateFormat formatarDate = new SimpleDateFormat("dd/MM/yyyy");
        for (Venda v : this.listaVendas) {
            if (v.getCliente() == null) {
                if (v.getSituacao() == 'P') {
                    modeloTabela.addRow(new Object[]{v.getNrNotaFiscal(), "Não Identificado", v.getValorTotal(), "Paga", formatarDate.format(v.getData())});
                } else {
                    modeloTabela.addRow(new Object[]{v.getNrNotaFiscal(), "Não Identificado", v.getValorTotal(), "Em Aberto", formatarDate.format(v.getData())});
                }

            } else {
                if (v.getSituacao() == 'P') {
                    modeloTabela.addRow(new Object[]{v.getNrNotaFiscal(), v.getCliente(), v.getValorTotal(), "Paga", formatarDate.format(v.getData())});
                } else {
                    modeloTabela.addRow(new Object[]{v.getNrNotaFiscal(), v.getCliente(), v.getValorTotal(), "Em Aberto", formatarDate.format(v.getData())});
                }
            }
        }
    }

