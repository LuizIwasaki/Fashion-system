    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtDataPesquisa.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(this, "O campo da data deve ser preenchido", "Erro", JOptionPane.WARNING_MESSAGE);
        } else {
            SimpleDateFormat formatarDate = new SimpleDateFormat("dd/MM/yyyy");
            try {
                atualizarTabela(contVenda.listarPorData(formatarDate.parse(txtDataPesquisa.getText())));
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "Data invalida\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        BuscaCliente bc = new BuscaCliente(null, true);
        bc.setVisible(true);
        if (bc.isConfirmou()) {
            Cliente c = bc.getClienteSelecionado();
            txtNome.setText(c.getNome());
            atualizarTabela(contVenda.listarPorCliente(c.getId()));
        }
    }//GEN-LAST:event_jButton3ActionPerformed

