    public void setDados(Compra c){
        txtNrNota.setText(String.valueOf(c.getNrNotaFiscal()));
        SimpleDateFormat formatarDate = new SimpleDateFormat("dd/MM/yyyy");
        txtData.setText(String.valueOf(formatarDate.format(c.getData())));
        if(c.getFornecedor()==null){
            txtFornecedor.setText("NÃ£o Identificado");
        }else{
            txtFornecedor.setText(String.valueOf(c.getFornecedor()));
        }
        txtValorDesconto.setText(String.valueOf(c.getDesconto()));
        txtValorFinal.setText(String.valueOf(c.getValorTotal()));
        
        DefaultTableModel modeloTabela;
        modeloTabela = (DefaultTableModel) tabItens.getModel();

        while (modeloTabela.getRowCount() > 0) {
            modeloTabela.removeRow(0);
        }
        
        for (ItemCompra ic : c.getListaItens()) {
            modeloTabela.addRow(new Object[]{ic.getRoupa(), ic.getRoupa().getValorCompra(), ic.getQuantidade(), ic.getSubTotal()});
        }
    }
    
