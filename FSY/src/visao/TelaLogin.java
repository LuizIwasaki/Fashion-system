    public void administradorInicial() {
        ControleFuncionario ct = new ControleFuncionario();
        if (ct.listarTodos().isEmpty()) {
            OperacoesFuncionario of = new OperacoesFuncionario(this, true);
            of.setCadastroInicial();
            of.setVisible(true);
            if (of.isConfirmou()) {
                ct.salvar(of.getFuncionario());
            } else {
                int r = JOptionPane.showConfirmDialog(this, "O administrador inicial deve ser cadastrado", "Obrigat√≥rio", JOptionPane.OK_CANCEL_OPTION);
                if (r == 0) {
                    this.administradorInicial();
                } else {
                    this.dispose();
                }
            }
        }
    }

