    private void jPanel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel4MouseClicked
        if (txtDataFinal.getText().equals("  /  /    ") || txtDataInicial.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(this, "As duas datas devem ser preenchidas", "Erro", JOptionPane.WARNING_MESSAGE);
        } else {
            SimpleDateFormat formatarDate = new SimpleDateFormat("dd/MM/yyyy");
            try {
                Date dataInicial = formatarDate.parse(txtDataInicial.getText());
                Date dataFinal = formatarDate.parse(txtDataFinal.getText());
                try {
                    InputStream inputStream = getClass().getResourceAsStream("/relatorio/fluxoDeCaixa.jrxml");
                    JasperDesign jasperDesign = JRXmlLoader.load(inputStream);
                    JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);

                    EntityManager entityManager = new ControleCaixa().getEntityManager();

                    HashMap<String, Object> parametros = new HashMap<>();
                    parametros.put(JRJpaQueryExecuterFactory.PARAMETER_JPA_ENTITY_MANAGER, entityManager);
                    java.sql.Date dataI = new java.sql.Date(dataInicial.getTime());
                    java.sql.Date dataF = new java.sql.Date(dataFinal.getTime());
                    parametros.put("dataInicial", dataI);
                    parametros.put("dataFinal", dataF);

                    JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parametros);
                    JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);
                    jasperViewer.setVisible(true);
                    this.dispose();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }

            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "Data invalida\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jPanel4MouseClicked

