    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<Cliente> LC = contCliente.listarPorNomePesquisado(txtPesquisa.getText());
        if (LC.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum cliente foi encontrado.", "NÃ£o Encontrado", JOptionPane.WARNING_MESSAGE);
            atualizarTabela(contCliente.listarTodos());
        } else {
            atualizarTabela(LC);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linha = tabCliente.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this,
                    "Selecione um cliente na tabela");
        } else {
            clienteSelecionado = listaClientes.get(linha);
            confirmou = true;
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        OperacoesCliente oc = new OperacoesCliente(null,true);
        oc.setCadastro();
        oc.setVisible(true);

        if (oc.isConfirmou()) {
            contCliente.salvar(oc.getCliente());
            atualizarTabela(contCliente.listarTodos());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

