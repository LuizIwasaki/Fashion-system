    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<Fornecedor> LF = contFornecedor.listarPorNomePesquisado(txtPesquisa.getText());
        if (LF.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum fornecedor foi encontrado.", "NÃ£o Encontrado", JOptionPane.WARNING_MESSAGE);
            atualizarTabela(contFornecedor.listarTodos());
        } else {
            atualizarTabela(LF);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linha = tabFornecedor.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this,
                "Selecione um cliente na tabela");
        } else {
            fornecedorSelecionado = listaFornecedores.get(linha);
            confirmou = true;
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        OperacoesFornecedor of = new OperacoesFornecedor(null,true);
        of.setCadastro();
        of.setVisible(true);
        
        if(of.isConfirmou()){
            contFornecedor.salvar(of.getFornecedor());
            atualizarTabela(contFornecedor.listarTodos());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

