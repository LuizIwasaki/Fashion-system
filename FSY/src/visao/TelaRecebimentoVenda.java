    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        BuscaCliente bc = new BuscaCliente(null, true);
        bc.setVisible(true);
        if (bc.isConfirmou()) {
            Cliente c = bc.getClienteSelecionado();
            txtNome.setText(c.getNome());
            atualizarTabela(contVenda.listarPorClienteN(c.getId()));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linha = tabVenda.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this,
                    "Selecione uma venda na tabela");
        } else {
            Venda v = listaVendas.get(linha);
            QuitarRecebimento qr = new QuitarRecebimento(this, true);
            qr.preencheTabela(v);
            qr.setVisible(true);
            
            atualizarTabela(contVenda.listarPorClienteN(v.getCliente().getId()));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

