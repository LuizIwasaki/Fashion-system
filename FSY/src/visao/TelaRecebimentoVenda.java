    public void atualizarTabela(List<Venda> lista) {
        DefaultTableModel modeloTabela;
        modeloTabela = (DefaultTableModel) tabVenda.getModel();

        while (modeloTabela.getRowCount() > 0) {
            modeloTabela.removeRow(0);
        }

        listaVendas.clear();

        listaVendas.addAll(lista);
        SimpleDateFormat formatarDate = new SimpleDateFormat("dd/MM/yyyy");
        for (Venda v : this.listaVendas) {
            modeloTabela.addRow(new Object[]{v.getNrNotaFiscal(), v.getCliente(), formatarDate.format(v.getData()), v.getValorTotal()});
        }
    }

